        <h1>3D Wireless Network Simulator README</h1>
        <h2>Introduction</h2>
        <p>This 3D Wireless Network Simulator is a powerful educational and experimental tool that allows users to observe the behavior of wireless signals in a dynamic, three-dimensional environment.</p>
        <p>This simulator uses a Signal-to-Noise Ratio (SNR) based approach to depict spectral efficiency, modulation, and coding rate, offering a deeper understanding of the concepts involved in wireless network communications.</p>

        <h2>Features</h2>
        <ul>
            <li>Dynamic 3D visualization: Depicts the Signal-to-Noise Ratio (SNR) as a set of three-dimensional cubes, providing a unique visual approach to understanding wireless signals.</li>
            <li>SNR-Based Modulation and Coding: The application uses SNR to compute and display Spectral Efficiency, Modulation, and Coding Rate.</li>
            <li>Interactive: Clicking on the displayed cubes toggles a detailed display of the SNR and other pertinent data.</li>
        </ul>

        <h2>How to Use</h2>
        <ol>
            <li>Open the application in your web browser.</li>
            <li>The 3D simulation will automatically start, presenting a grid of cubes that represent different SNR values.</li>
            <li>Click on any cube to reveal detailed information about its SNR, spectral efficiency, modulation, and coding rate.</li>
        </ol>

